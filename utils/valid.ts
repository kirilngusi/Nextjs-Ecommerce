
export const validLogin= (username:string, password:string) => {
    if(!username || !password) {
        return 'Please add all fields';
    }
    return;
}

export const validRegis= (name:string, username:string, password:string) => {
    if(!name || !username || !password) {
        return 'Please add all fields';
    }
    return;
}


